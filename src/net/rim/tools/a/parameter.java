package net.rim.tools.a;

public final class parameter
{
	 public String _name;
	 public String _typeName;
	
	public parameter()
	{
		_name = "";
		_typeName = "";
	}
	
	public String getTypeName()
	{
		return _typeName;
	}
	
	public void setTypeName(String __string)
	{
		_typeName = __string;
	}

	public String getName()
	{
		return _name;
	}
	
	public void setName(String __string)
	{
		_name = __string;
	}
	
	
}
